        マネージャーがこれを見て、問題がなく採用となれば、ブランチ内の変更点を含む部分をホストと置き換え統合していきます(Marge)。<br />
        これにより、各々の開発者による直接のコミットによるホストの崩壊を防ぎつつ、効率の良い開発を行うことが可能になります。
      </p>

