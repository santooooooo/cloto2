        意図せずプログラムが壊れてしまうことがあります。 これを防ぐために、開発者ごとにMaster
        Branchの複製とも言えるブランチ(Remote Branch)を割り当てます。<br />
        まず各々の開発者は自身に割り当てられたブランチごとにローカルリポジトリに開発、変更を加えていきます(コミット)。<br />
